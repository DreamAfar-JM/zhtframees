package com.zht.common.generator.model;

public class EntityViewModel {
	
	
	public EntityViewModel(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	private String id;
	private String name;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
